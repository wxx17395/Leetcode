from collections import deque

class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None

class Solution:
    def levelOrder(self, root: TreeNode) -> list:
        if not root:
            return []
        queue, res, flag = deque(), [], 1
        queue.append(root)
        while queue:
            cur = []
            for i in range(len(queue)):
                node = queue.popleft()
                cur.append(node.val)
                if node.left:
                    queue.append(node.left)
                if node.right:
                    queue.append(node.right)
            if not flag:
                cur.reverse()
            flag = flag ^ 1
            res.append(cur)
        return res

if __name__ == '__main__':
    root = TreeNode(3)
    left = TreeNode(9)
    right = TreeNode(20)
    root.left = left
    root.right = right
    print(Solution().levelOrder(root))